     public void LoadStartplace()
    {
        try
        {
            pst = con.prepareStatement(" SELECT startplace FROM  traindetails ");
            rs = pst.executeQuery();
            txtsplace1.removeAllItems();
            
            while ( rs.next())
            {
                txtsplace1.addItem(rs.getString(1));
            }
        }
        catch ( Exception e)
        {
            e.printStackTrace();
        }
    }
    
    
