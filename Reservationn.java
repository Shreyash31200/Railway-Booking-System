       public void load()
     {
         String splace = txtsplace1.getSelectedItem().toString();
         String eplace = txtdplace.getSelectedItem().toString();
         try
         {
             pst = con.prepareStatement(" SELECT * FROM traindetails where startplace = ? and destinationplace = ?");
             pst.setString(1, splace);
              pst.setString(2, eplace);
              rs = pst.executeQuery();
              
              if( rs.next() == true )
              {
                  txttno.setText(rs.getString(1));
                  txttname.setText(rs.getString(2));
                  txtprice.setText(rs.getString(5));
              }
              else
              {
                   JOptionPane.showMessageDialog(this, "Train not available");
              }
         }
         catch ( Exception e )
         {
             e.printStackTrace();
         }
         
     }
      
    
    
    
    
    
    
    
    
    
    
    
